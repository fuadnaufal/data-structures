var buah = ["Apel", "Mangga", "Pisang", "Jeruk"];
// MENAMBAH DAN MENGHAPUS ELEMEN
buah.push("Semangka"); // Tambah di akhir
console.log("Setelah push:", buah);
buah.unshift("Durian"); // Tambah di awal
console.log("Setelah unshift:", buah);
buah.pop(); // Hapus elemen terakhir
console.log("Setelah pop:", buah);
buah.shift(); // Hapus elemen pertama
console.log("Setelah shift:", buah);